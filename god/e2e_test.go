// E2E: Thrash detection and recovery
// ---------------------------------------------------------------------------

func TestE2EThrashDetectionAndRecovery(t *testing.T) {
	dataDir := t.TempDir()
	srv, err := heaven.NewServer(dataDir)
	if err != nil {
		t.Fatalf("heaven server init: %v", err)
	}
	ts := httptest.NewServer(srv)
	t.Cleanup(ts.Close)

	client := NewHeavenClient(ts.URL)
	metricsAgg := NewMetricsAggregator(client)

	config := DefaultThrashConfig()
	config.MaxRejects = 2
	thrashDetector := NewThrashDetector(config, client)

	// Simulate a mission that gets rejected repeatedly
	missionID := genID()
	metricsAgg.StartMission(missionID)

	// Reject 1
	metricsAgg.RecordReject(missionID)
	metricsAgg.EndTurn(missionID)
	m := metricsAgg.Get(missionID)
	result := thrashDetector.Check(m)
	if result.Thrashing {
		t.Error("should not thrash after 1 reject")
	}

	// Reject 2 — triggers thrash
	metricsAgg.RecordReject(missionID)
	metricsAgg.EndTurn(missionID)
	m = metricsAgg.Get(missionID)
	result = thrashDetector.Check(m)
	if !result.Thrashing {
		t.Fatal("should thrash after 2 rejects")
	}
	t.Logf("Thrash detected: %s", result.Reason)

	// Set up oracle for recovery with a proper OracleResponse
	oracleRespJSON, _ := json.Marshal(OracleResponse{
		UpdatedDAG: &MissionDAG{
			PlanID:   "recovery-plan",
			TaskDesc: "Recovered from thrash",
			Nodes: []DAGNode{
				{Mission: Mission{MissionID: "recovery-m1", Goal: "Fix root cause", BaseRev: "HEAD", Tasks: []string{"fix"}, TokenBudget: 8000, CreatedAt: time.Now().UTC().Format(time.RFC3339)}},
			},
			CreatedAt: time.Now().UTC().Format(time.RFC3339),
		},
		LeasesPlan:       []Scope{{ScopeType: "file", ScopeValue: "sample.go"}},
		RiskHotspots:     []string{"sample.go"},
		RecommendedTests: []string{"go test ./..."},
	})
	oracleProvider := &e2eProvider{responses: map[string][]byte{}}
	// Override Send to return oracle response for any pack
	oracle := NewOracle(OracleConfig{Enabled: true, Provider: &mockOracleProvider{response: oracleRespJSON}}, client)

	oracleResp, err := oracle.EscalateOnThrash(missionID, m, "spec-blob-e2e")
	if err != nil {
		t.Fatalf("oracle escalate: %v", err)
	}
	if oracleResp == nil {
		t.Fatal("expected oracle response for thrashing mission")
	}
	t.Logf("Oracle returned replacement plan: %s with %d nodes", oracleResp.UpdatedDAG.PlanID, len(oracleResp.UpdatedDAG.Nodes))

	_ = oracleProvider // provider prepared for oracle

	// Verify events trail
	events, err := client.TailEvents(100)
	if err != nil {
		t.Fatalf("tail events: %v", err)
	}

	hasThrash := false
	hasMetrics := false
	for _, raw := range events {
		var evt map[string]any
		json.Unmarshal(raw, &evt)
		switch evt["type"] {
		case "mission_thrashing":
			hasThrash = true
		case "metrics_snapshot":
			hasMetrics = true
		}
	}
	if !hasThrash {
		t.Error("expected mission_thrashing event in log")
	}

	// Complete the mission and check final metrics
	metricsAgg.CompleteMission(missionID)
	if !hasMetrics {
		// Check again after completion
		events, _ = client.TailEvents(100)
		for _, raw := range events {
			var evt map[string]any
			json.Unmarshal(raw, &evt)
			if evt["type"] == "metrics_snapshot" {
				hasMetrics = true
			}
		}
	}
	if !hasMetrics {
		t.Error("expected metrics_snapshot event after completion")
	}

	t.Logf("Final metrics:\n%s", metricsAgg.Summary(missionID))
}

// ---------------------------------------------------------------------------
// E2E: Receipt and gate merge
// ---------------------------------------------------------------------------

func TestE2EReceiptAndGate(t *testing.T) {
	dataDir := t.TempDir()
	srv, err := heaven.NewServer(dataDir)
	if err != nil {
		t.Fatalf("heaven server init: %v", err)
	}
	ts := httptest.NewServer(srv)
	t.Cleanup(ts.Close)

	client := NewHeavenClient(ts.URL)
	verifier := NewVerifier(client)

	// Pass case
	verifier.runCmd = mockRunCmd("all tests passed\n", 0)
	passResult, err := verifier.Verify(VerifyRequest{
		MissionID: "e2e-pass",
		RepoRoot:  t.TempDir(),
		Command:   "echo pass",
	})
	if err != nil {
		t.Fatalf("verify pass: %v", err)
	}
	if !passResult.Passed {
		t.Error("expected pass")
	}
	if err := GateMerge(passResult.Receipt); err != nil {
		t.Errorf("gate should pass: %v", err)
	}

	// Fail case
	verifier.runCmd = mockRunCmd("FAIL\n", 1)
	failResult, err := verifier.Verify(VerifyRequest{
		MissionID: "e2e-fail",
		RepoRoot:  t.TempDir(),
		Command:   "echo fail",
	})
	if err != nil {
		t.Fatalf("verify fail: %v", err)
	}
	if failResult.Passed {
		t.Error("expected fail")
	}
	if err := GateMerge(failResult.Receipt); err == nil {
		t.Error("gate should block failed verification")
	}

	// Verify receipts are stored in Heaven
	if passResult.BlobID == "" {
		t.Error("pass receipt should have blob ID")
	}
	if failResult.BlobID == "" {
		t.Error("fail receipt should have blob ID")
	}

	// Verify receipt timestamps
	for _, r := range []VerifyResult{*passResult, *failResult} {
		if _, err := time.Parse(time.RFC3339, r.Receipt.Timestamp); err != nil {
			t.Errorf("invalid timestamp: %v", err)
		}
		if r.Receipt.EnvHash == "" {
			t.Error("receipt should have env hash")
		}
		if r.Receipt.CommandHash == "" {
			t.Error("receipt should have command hash")
		}
		if r.Receipt.StdoutHash == "" {
			t.Error("receipt should have stdout hash")
		}
	}

	// Check events
	events, _ := client.TailEvents(100)
	verifyEvents := 0
	for _, raw := range events {
		var evt map[string]any
		json.Unmarshal(raw, &evt)
		if evt["type"] == "verification_complete" {
			verifyEvents++
		}
	}
	if verifyEvents != 2 {
		t.Errorf("expected 2 verification_complete events, got %d", verifyEvents)
	}
}

