// E2E: No repo dump — mission packs respect token budget
// ---------------------------------------------------------------------------

func TestE2ENoRepoDump(t *testing.T) {
	dataDir := t.TempDir()
	srv, err := heaven.NewServer(dataDir)
	if err != nil {
		t.Fatalf("heaven server init: %v", err)
	}
	ts := httptest.NewServer(srv)
	t.Cleanup(ts.Close)

	client := NewHeavenClient(ts.URL)

	// Index fixture repo
	fixtureDir, _ := filepath.Abs("../fixtures")
	if _, err := os.Stat(filepath.Join(fixtureDir, "sample.go")); os.IsNotExist(err) {
		t.Skipf("fixtures not found at %s", fixtureDir)
	}
	client.IRBuild(fixtureDir)

	// Plan
	planner := NewPlanner(client)
	dag, err := planner.Plan("Add a pow operation", fixtureDir)
	if err != nil {
		t.Fatalf("plan: %v", err)
	}

	// Compile packs and verify budget constraints
	pc := NewPromptCompiler(ts.URL + "/pf")

	for _, node := range dag.Nodes {
		m := node.Mission

		// Fetch candidate shards for this mission
		candidates := []CandidateShard{}
		for _, scope := range m.Scopes {
			if scope.ScopeType == "symbol" {
				candidates = append(candidates, CandidateShard{
					Kind:    "symdef",
					BlobID:  "blob-" + scope.ScopeValue,
					Content: []byte(`{"symbol":"` + scope.ScopeValue + `","definition":"func ` + scope.ScopeValue + `() {}"}`),
					Symbol:  scope.ScopeValue,
				})
			}
		}

		pack, err := pc.Compile(m, candidates)
		if err != nil {
			t.Fatalf("compile mission %s: %v", m.MissionID[:8], err)
		}

		// Pack size must not exceed 32KB
		packJSON, _ := json.Marshal(pack)
		if len(packJSON) > 32*1024 {
			t.Errorf("mission %s: pack size %d exceeds 32KB", m.MissionID[:8], len(packJSON))
		}

		// Total tokens must not exceed budget
		if pack.BudgetMeta.TotalTokens > pack.BudgetMeta.TokenBudget {
			t.Errorf("mission %s: total tokens %d exceeds budget %d",
				m.MissionID[:8], pack.BudgetMeta.TotalTokens, pack.BudgetMeta.TokenBudget)
		}

		// No single shard should be > 50% of budget
		halfBudget := m.TokenBudget / 2
		for _, shard := range pack.InlineShards {
			shardTokens := EstimateTokens(shard.Content)
			if shardTokens > halfBudget {
				t.Errorf("mission %s: shard %s (%d tokens) exceeds 50%% of budget (%d)",
					m.MissionID[:8], shard.Kind, shardTokens, m.TokenBudget)
			}
		}

		// No sensitive files in shards
		for _, shard := range pack.InlineShards {
			content := string(shard.Content)
			if strings.Contains(content, ".env") || strings.Contains(content, "credentials.json") {
				t.Errorf("mission %s: shard contains sensitive file reference", m.MissionID[:8])
			}
		}
	}
}

